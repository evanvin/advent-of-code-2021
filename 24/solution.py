def instruction(i, num, alu):
    if "inp" in i[0]:
        alu[i[1]] = num.pop(0)
    elif "add" in i[0]:
        b = int(i[2]) if (i[2].strip("-")).isnumeric() else alu[i[2]]
        alu[i[1]] += b
    elif "mul" in i[0]:
        b = int(i[2]) if (i[2].strip("-")).isnumeric() else alu[i[2]]
        alu[i[1]] *= b
    elif "div" in i[0]:
        b = int(i[2]) if (i[2].strip("-")).isnumeric() else alu[i[2]]
        if b != 0:
            alu[i[1]] = int(alu[i[1]] / b)
    elif "mod" in i[0]:
        b = int(i[2]) if (i[2].strip("-")).isnumeric() else alu[i[2]]
        if alu[i[1]] < 0 or b <= 0:
            pass
        alu[i[1]] %= b
    else:
        b = int(i[2]) if (i[2].strip("-")).isnumeric() else alu[i[2]]
        alu[i[1]] = 1 if alu[i[1]] == b else 0

    return num, alu


